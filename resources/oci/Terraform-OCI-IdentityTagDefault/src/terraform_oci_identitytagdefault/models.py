from cloudformation_cli_python_lib.interface import (
    BaseResourceHandlerRequest,
    BaseResourceModel,
)

T = TypeVar("T")


def set_or_none(value: Optional[Sequence[T]]) -> Optional[AbstractSet[T]]:
    if value:
        return set(value)
    return None


@dataclass
class ResourceHandlerRequest(BaseResourceHandlerRequest):
    # pylint: disable=invalid-name
    desiredResourceState: Optional["ResourceModel"]
    previousResourceState: Optional["ResourceModel"]


@dataclass
class ResourceModel(BaseResourceModel):
    tfcfnid: Optional[str]
    CompartmentId: Optional[str]
    IsRequired: Optional[bool]
    State: Optional[str]
    TagDefinitionId: Optional[str]
    TagDefinitionName: Optional[str]
    TagNamespaceId: Optional[str]
    TimeCreated: Optional[str]
    Value: Optional[str]
    Timeouts: Optional["_Timeouts"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            CompartmentId=json_data.get("CompartmentId"),
            IsRequired=json_data.get("IsRequired"),
            State=json_data.get("State"),
            TagDefinitionId=json_data.get("TagDefinitionId"),
            TagDefinitionName=json_data.get("TagDefinitionName"),
            TagNamespaceId=json_data.get("TagNamespaceId"),
            TimeCreated=json_data.get("TimeCreated"),
            Value=json_data.get("Value"),
            Timeouts=Timeouts._deserialize(json_data.get("Timeouts")),
        )


# work around possible type aliasing issues when variable has same name as a model
_ResourceModel = ResourceModel


@dataclass
class Timeouts:
    Create: Optional[str]
