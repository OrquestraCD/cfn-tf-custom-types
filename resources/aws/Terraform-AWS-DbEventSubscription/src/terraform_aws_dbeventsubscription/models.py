@dataclass
class ResourceModel(BaseResourceModel):
    tfcfnid: Optional[str]
    Arn: Optional[str]
    CustomerAwsId: Optional[str]
    Enabled: Optional[bool]
    EventCategories: Optional[Sequence[str]]
    Id: Optional[str]
    Name: Optional[str]
    NamePrefix: Optional[str]
    SnsTopic: Optional[str]
    SourceIds: Optional[Sequence[str]]
    SourceType: Optional[str]
    Tags: Optional[Sequence["_Tags"]]
    Timeouts: Optional["_Timeouts"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            Arn=json_data.get("Arn"),
            CustomerAwsId=json_data.get("CustomerAwsId"),
            Enabled=json_data.get("Enabled"),
            EventCategories=json_data.get("EventCategories"),
            Id=json_data.get("Id"),
            Name=json_data.get("Name"),
            NamePrefix=json_data.get("NamePrefix"),
            SnsTopic=json_data.get("SnsTopic"),
            SourceIds=json_data.get("SourceIds"),
            SourceType=json_data.get("SourceType"),
            Tags=json_data.get("Tags"),
            Timeouts=Timeouts._deserialize(json_data.get("Timeouts")),
        )


# work around possible type aliasing issues when variable has same name as a model
_ResourceModel = ResourceModel


