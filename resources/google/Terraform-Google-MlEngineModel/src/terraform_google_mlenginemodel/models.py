    OnlinePredictionConsoleLogging: Optional[bool]
    OnlinePredictionLogging: Optional[bool]
    Project: Optional[str]
    Regions: Optional[Sequence[str]]
    DefaultVersion: Optional[Sequence["_DefaultVersion"]]
    Timeouts: Optional["_Timeouts"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            Description=json_data.get("Description"),
            Id=json_data.get("Id"),
            Labels=json_data.get("Labels"),
            Name=json_data.get("Name"),
            OnlinePredictionConsoleLogging=json_data.get("OnlinePredictionConsoleLogging"),
            OnlinePredictionLogging=json_data.get("OnlinePredictionLogging"),
            Project=json_data.get("Project"),
            Regions=json_data.get("Regions"),
            DefaultVersion=json_data.get("DefaultVersion"),
            Timeouts=Timeouts._deserialize(json_data.get("Timeouts")),
        )


