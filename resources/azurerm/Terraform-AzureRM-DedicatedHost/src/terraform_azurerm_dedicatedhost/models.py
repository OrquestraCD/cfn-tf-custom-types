@dataclass
class ResourceModel(BaseResourceModel):
    tfcfnid: Optional[str]
    AutoReplaceOnFailure: Optional[bool]
    DedicatedHostGroupId: Optional[str]
    Id: Optional[str]
    LicenseType: Optional[str]
    Location: Optional[str]
    Name: Optional[str]
    PlatformFaultDomain: Optional[float]
    SkuName: Optional[str]
    Tags: Optional[Sequence["_Tags"]]
    Timeouts: Optional["_Timeouts"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            AutoReplaceOnFailure=json_data.get("AutoReplaceOnFailure"),
            DedicatedHostGroupId=json_data.get("DedicatedHostGroupId"),
            Id=json_data.get("Id"),
            LicenseType=json_data.get("LicenseType"),
            Location=json_data.get("Location"),
            Name=json_data.get("Name"),
            PlatformFaultDomain=json_data.get("PlatformFaultDomain"),
            SkuName=json_data.get("SkuName"),
            Tags=json_data.get("Tags"),
            Timeouts=Timeouts._deserialize(json_data.get("Timeouts")),
        )


# work around possible type aliasing issues when variable has same name as a model
